PUBLIC RENDER
EXTRN X:BYTE

DS2 SEGMENT AT 0B800h
	CA LABEL BYTE
	ORG 80 * 2 * 2 + 1 * 2
	Z LABEL WORD
	
DS2 ENDS

CSEG SEGMENT PARA PUBLIC 'CODE'
	ASSUME CS:CSEG
	RENDER PROC NEAR
		MOV ax, ds2
		MOV es, ax
		MOV al, X
		MOV ah, 2
		MOV es:Z,ax
		RET
    RENDER ENDP

CSEG ENDS
END