SSTACK SEGMENT PARA STACK  'STACK'
	DB 64 DUP('ssssssss')
SSTACK ENDS

DSEG1 SEGMENT PARA COMMON 'DATA'
	S1 DB '<'
	DB 65535 - 1024*60 DUP (1)
DSEG1 ENDS

DSEG2 SEGMENT PARA COMMON 'DATA'
	S2 DB '^'
	DB 65535 - 1024*60 DUP (1)
DSEG2 ENDS

DSEG3 SEGMENT PARA COMMON 'DATA'
	S3 DB '>'
	DB 65535 - 1024*60 DUP (1) ; segment near (or at) 64k limit
DSEG3 ENDS

CSEG SEGMENT PARA PUBLIC 'CODE'
	ASSUME CS:CSEG, DS:DSEG1, SS:SSTACK
	START PROC FAR
		MOV AX, DSEG1
		MOV DS, AX
		MOV DL, S1
		MOV AH, 2
		INT 21h
		MOV DL, 13
		INT 21h
		MOV DL, 10
		INT 21h
		
		ASSUME DS:DSEG2
		MOV AX, DSEG2
		MOV DS, AX
		MOV DL, S2
		MOV AH, 2
		INT 21h
		MOV DL, 13
		INT 21h
		MOV DL, 10
		INT 21h
		
		ASSUME DS:DSEG3
		MOV AX, DSEG3
		MOV DS, AX
		MOV DL, S3
		MOV AH, 2
		INT 21h
		MOV DL, 13
		INT 21h
		MOV DL, 10
		INT 21h
		
		MOV AH, 4ch
		MOV AL, 0
		INT 21h
	START ENDP
CSEG ENDS

